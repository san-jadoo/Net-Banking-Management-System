    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: 
        String st = dr;
        Connection con = null ;
        PreparedStatement pstmt = null ;
        ResultSet rs = null ;
        try
            {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/netbank","root","");
                String str = "Select * from data where accno = "+st +" " ;
                pstmt = con.prepareStatement(str);  
                rs = pstmt.executeQuery();
                while(rs.next())
                {
                    String am1 = rs.getString("netamt");
                    float a = Float.parseFloat(am1); 
                    String am = amt.getText();
                    Float i = Float.parseFloat(am);
                    float k ;
                    k = (a + i) ; 
                    String q1 = "Update data SET netamt = '"+k +"' where accno = '"+st +"' ";
                    Statement stmt = con.createStatement(); 
                    stmt.executeUpdate(q1); 
                    JOptionPane.showMessageDialog(null, "Amount deposited Successfully" ); 
                    JOptionPane.showMessageDialog(null, "Your Current Balance Amount : "+k );
                    amt.setText("");
                } 
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
                JOptionPane.showMessageDialog(null,"Amount not Deposited");
            } 
    }//GEN-LAST:event_jButton1ActionPerformed

