        // TODO add your handling code here:
        
        try
        { 
            String a1 = mailiid.getText();
            int a = a1.indexOf("@");
            int b = a1.indexOf(".");
            int g = phhno.getText().length();   
            PreparedStatement pstmt = null ;
            ResultSet rs = null ;
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/netbank","root","");
            Statement stmt = con.createStatement();
            String str = "Select * from admindata where aid = '"+er +"' "; 
            if(namee.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Name is Mandatory");
            }   
            else if(aadha.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Aadhar Number is Mandatory");
            }
            else if(mailiid.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Mail ID is Mandatory");
            } 
            else if(a==-1||a==0||a==mailiid.getText().length()||b==-1||b==0||b==mailiid.getText().length())
            {
                JOptionPane.showMessageDialog(null, "Invalid Mail ID.");
            }
            else if((phhno.getText()).equals(""))
            {
                JOptionPane.showMessageDialog(null, "Phone Number is Mandatory");
            }
            else if(g>10||g<10)
            {
                JOptionPane.showMessageDialog(null,"Phone Numbers must contain 10 digits only");
            }
            else if(addres.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Address is Mandatory");
            }
            else
            {
                String z = er;
                String gen = null;
                if(male.isSelected())
                    gen="male";
                else if(female.isSelected())
                    gen="Female";
                String q2 = "UPDATE `admindata` SET `name`='"+namee.getText() +"' ,`mailid`='"+mailiid.getText() +"',`phno`='"+phhno.getText() +"' ,`gender`='"+gen +"' ,`aadh`='"+aadha.getText() +"' ,`address`= '"+addres.getText() +"' WHERE aid = '"+z +"' ";
                
                stmt.executeUpdate(q2);
                JOptionPane.showMessageDialog(null,"Updated Successfully!"); 
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Not Updated!");
        }
