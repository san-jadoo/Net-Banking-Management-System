    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try
        {    
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/netbank","root","");
            String str = "Select * from admindata where aid = '"+er +"' ";
            Statement st = con.createStatement(); 
            ResultSet rs = st.executeQuery(str);
            while(rs.next())
            {
                String na = rs.getString("name");
                String ph = rs.getString("phno");
                String id = rs.getString("mailid");
                String ad = rs.getString("aadh");
                String ar = rs.getString("address");
                String ge = rs.getString("gender"); 
                namee.setText(na);
                phhno.setText(ph);
                mailiid.setText(id);
                aadha.setText(ad);
                addres.setText(ar); 
                if(ge.equals("Male"))
                    male.isSelected();
                else if(ge.equals("Female"))
                    male.isSelected(); 
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

